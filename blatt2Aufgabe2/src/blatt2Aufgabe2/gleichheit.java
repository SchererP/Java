package blatt2Aufgabe2;

public class gleichheit {
	public static  void main(String[] args){
		float f1 = 6.5f;
		float f2 = 6.4f;
		if(f1 == 6.5){
			System.out.println("6.5 is equal 6.5 as float");
		}else{
			System.out.println("6.5 is Not equal 6.5 as float");
		}
		if(f2 == 6.4){
			System.out.println("6.4 is equal 6.4 as float");
		}else{
			System.out.println("6.4 is Not equal 6.4 as float");
		}
	}
}
